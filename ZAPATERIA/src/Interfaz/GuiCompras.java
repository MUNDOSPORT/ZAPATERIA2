    private void txtCodigodeBarraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigodeBarraKeyTyped
        char c = evt.getKeyChar();
        if (c == KeyEvent.VK_ENTER) {
            txtCantidad.requestFocus();
        }
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
        }
        if (txtCodigodeBarra.getText().length() >= 13) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCodigodeBarraKeyTyped

