    public void preparado() {
        if (!txtCodigodeBarra.getText().equals("")) {
            if ((unaEmpresa.getArticulos().containsKey(txtCodigodeBarra.getText()))) {
                DefaultTableModel modelo = (DefaultTableModel) tblVenta.getModel();
                int filas = tblVenta.getRowCount();
                Boolean error = false;
                for (int i = 0; filas > i; i++) {
                    if (txtCodigodeBarra.getText().equals(modelo.getValueAt(i, 0).toString()) && modificar == false) {
                        error = true;
                    }
                }
                if (error) {
                    JOptionPane.showMessageDialog(null, "El articulo ya esta cargado");
                    lblNombre.setText("El articulo ya esta cargado");
                    lblNombre.setForeground(Color.red);
                    lblMarca.setText("");
                    btnAgregar.setEnabled(false);
                } else {
                    txtCodigodeBarra.setEnabled(false);
                    txtCantidad.setEnabled(true);
                    txtCantidad.setEnabled(true);
                    btnAgregar.setEnabled(true);
                    Map articulos = unaEmpresa.getArticulos();
                    Articulo unArt = (Articulo) articulos.get(txtCodigodeBarra.getText().toString());
                    btnAgregar.setEnabled(true);
                    lblMarca.setText("Nombre: " + unArt.getNombre());
                    lblMarca.setForeground(Color.green);
                    lblNombre.setText("Marca: " + unArt.getUnaMarca().getNombreMarca());
                    lblNombre.setForeground(Color.green);
                    txtPrecio.setText(String.valueOf(unArt.getPrecioVenta()));
                    txtPrecio.setText(String.valueOf(unArt.getPrecioVenta()));
                    txtCantidad.setText("");
                    stock = unArt.getStock();
                    jButton1.setEnabled(false);
                   // txtMonto1.setText("0.0");
                   // lblTotal1.setText(String.valueOf(Double.parseDouble(txtPrecio.getText()) - Double.parseDouble(txtMonto1.getText())));
                }
            } else {
                JOptionPane.showMessageDialog(null, "no existe articulo.");
                lblNombre.setText("no existe articulo");
                lblNombre.setForeground(Color.red);
                lblMarca.setText("");
                btnAgregar.setEnabled(false);
            }
        }
    }
